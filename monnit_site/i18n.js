  function translateTree(root) {
    if (LANG !== 'en' || !root) return;
    // 1) 헤드라인 요소 (innerHTML 교체)
    if (root.querySelectorAll) {
      var els = root.querySelectorAll(HEADLINE_SEL);
      for (var i = 0; i < els.length; i++) {
        var el = els[i], key = norm(el.textContent);
        if (HTML_DICT[key]) {
          if (el.__ko == null) el.__ko = el.innerHTML;
          el.innerHTML = HTML_DICT[key];
          handled.add(el);
        }
      }
    }
    // 2) 텍스트 노드
    var walker = document.createTreeWalker(root, NodeFilter.SHOW_TEXT, null, false);
    var batch = [], n;
    while ((n = walker.nextNode())) batch.push(n);
    batch.forEach(function (tn) {
      var p = tn.parentElement, skip = false;
      while (p) { if (handled.has(p)) { skip = true; break; } p = p.parentElement; }
      if (skip) return;
      var raw = tn.nodeValue, trimmed = raw.trim();
      if (!trimmed) return;
      var en = DICT[norm(raw)];
      if (en != null) {
        if (tn.__ko == null) tn.__ko = raw;
        tn.nodeValue = raw.replace(trimmed, en);
      }
    });
    // 3) placeholder 속성
    if (root.querySelectorAll) {
      var ph = root.querySelectorAll('[placeholder]');
      for (var j = 0; j < ph.length; j++) {
        var e = ph[j], en2 = DICT[norm(e.getAttribute('placeholder'))];
        if (en2 != null) { if (e.__koPh == null) e.__koPh = e.getAttribute('placeholder'); e.setAttribute('placeholder', en2); }
      }
    }
  }

